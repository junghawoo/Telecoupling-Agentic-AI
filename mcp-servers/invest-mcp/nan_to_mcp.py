def main() -> None:
    parser = argparse.ArgumentParser(
        description="Convert Nan's async tool modules to FastMCP @mcp.tool() functions.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "source",
        help="Path to a single Nan tool .py file OR a directory of tool modules.",
    )
    parser.add_argument(
        "--output", "-o",
        metavar="FILE",
        help="Write generated code to FILE instead of stdout.",
    )
    parser.add_argument(
        "--append-to", "-a",
        metavar="SERVER_PY",
        help=(
            "Append NEW tools to an existing server.py. "
            "Existing tools are skipped even if their signatures changed. "
            "Use --sync to also update changed tools."
        ),
    )
    parser.add_argument(
        "--sync",
        metavar="SERVER_PY",
        help=(
            "Full sync against an existing server.py:\n"
            "  • NEW tool    → appended before the entry-point block\n"
            "  • CHANGED tool → replaced in-place (signature diff shown)\n"
            "  • UNCHANGED   → skipped\n"
            "Use --dry-run to preview without writing."
        ),
    )
    parser.add_argument(
        "--skip", "-s",
        nargs="*",
        metavar="STEM",
        default=[],
        help="Module stems to skip (e.g. --skip cbc_main habitat_quality).",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would change without writing any file.",
    )
    parser.add_argument(
        "--include-duplicates",
        action="store_true",
        help="Also convert tools already present in the default ALREADY_IN_SERVER list.",
    )
    args = parser.parse_args()

    if args.append_to and args.sync:
        sys.exit("ERROR: --append-to and --sync are mutually exclusive.")

    source = Path(args.source).expanduser().resolve()

    # Collect input files
    if source.is_file():
        files = [source]
    elif source.is_dir():
        files = sorted(source.glob("*.py"))
        files = [f for f in files if f.name != "__init__.py"]
    else:
        sys.exit(f"ERROR: {source} is not a file or directory.")

    # Determine target server path and current signatures
    server_path: Optional[Path] = None
    existing_sigs: dict[str, tuple] = {}
    existing_names: set[str] = set()

    if args.append_to:
        server_path = Path(args.append_to).expanduser()
        existing_names = _existing_tools_in_file(server_path)
        print(f"[info] {len(existing_names)} tools already in {args.append_to}", file=sys.stderr)

    if args.sync:
        server_path = Path(args.sync).expanduser()
        existing_sigs  = _server_signatures(server_path)
        existing_names = set(existing_sigs.keys())
        print(f"[info] {len(existing_names)} tools in {args.sync} (sync mode)", file=sys.stderr)

    skip_stems: set[str] = set(args.skip)

    # ── Convert each file ────────────────────────────────────────────────────
    # append mode:  new_blocks → appended to server
    # sync mode:    new_blocks → appended;  updated_tools → replaced in-place
    new_blocks:      list[str]        = []   # code blocks to append
    updated_tools:   dict[str, str]   = {}   # fn_name → new code (sync mode only)
    dry_run_changes: int              = 0    # counter for dry-run reporting
    skipped:         list[str]        = []
    failed:          list[str]        = []

    for filepath in files:
        stem = filepath.stem

        if stem.startswith("_") or stem in ("utils", "config", "server"):
            continue

        fn_name = _fn_name_for_stem(stem)

        # --- static skip list ---
        if stem in skip_stems:
            skipped.append(f"  --skip:         {filepath.name}")
            continue
        if not args.include_duplicates and fn_name in ALREADY_IN_SERVER:
            if not args.sync:          # in sync mode we still want to update these
                skipped.append(f"  already in MCP: {filepath.name}")
                continue

        # --- append mode: skip if already in target ---
        if args.append_to and fn_name in existing_names:
            skipped.append(f"  already exists: {filepath.name}  (use --sync to update)")
            continue

        if args.dry_run and not args.sync:
            print(f"  would add: {filepath.name} → {fn_name}()")
            continue

        # --- generate the new tool code ---
        spec = parse_nan_tool(filepath)
        if spec is None:
            failed.append(filepath.name)
            continue
        block = generate_mcp_tool(spec)

        # --- sync mode: classify as new / changed / unchanged ---
        if args.sync:
            if fn_name not in existing_names:
                # Brand-new tool
                if args.dry_run:
                    print(f"  [+] {filepath.name} → {fn_name}()  (new, would append)")
                    dry_run_changes += 1
                    continue
                new_blocks.append(block)
                flag = " ⚠" if (not isinstance(stem, str) or
                                  getattr(spec, 'has_complex_logic', False)) else ""
                print(f"  [+]{flag} {filepath.name} → appended", file=sys.stderr)
            else:
                # Tool already exists — compare signatures
                _parsed_block = ast.parse(block)
                new_sig = _param_signature(next(
                    n for n in ast.walk(_parsed_block)
                    if isinstance(n, (ast.FunctionDef, ast.AsyncFunctionDef))
                    and n.name == fn_name
                ))
                old_sig = existing_sigs[fn_name]

                if new_sig == old_sig:
                    skipped.append(f"  [=] {filepath.name}  (unchanged)")
                    continue

                diff_desc = _signature_diff(old_sig, new_sig)
                if args.dry_run:
                    print(f"  [~] {filepath.name} → {fn_name}()  CHANGED: {diff_desc}")
                    dry_run_changes += 1
                    continue

                updated_tools[fn_name] = block
                print(f"  [~] {filepath.name} → {fn_name}()  updated ({diff_desc})", file=sys.stderr)
            continue

        # --- append / output mode ---
        new_blocks.append(block)
        kind = "async/custom" if getattr(spec, "is_custom_tool", False) else "invest"
        flag = " ⚠ review" if getattr(spec, "has_complex_logic", False) else ""
        print(f"  [ok]{flag} [{kind}]  {filepath.name} → {fn_name}()", file=sys.stderr)

    if args.dry_run:
        if dry_run_changes == 0:
            print("[dry-run] nothing to change")
        else:
            print(f"[dry-run] {dry_run_changes} change(s) would be applied — rerun without --dry-run to apply")
        return

    # ── Apply changes ────────────────────────────────────────────────────────

    # 1. In-place replacements (sync mode)
    if updated_tools and server_path:
        for fn_name, new_code in updated_tools.items():
            ok = _replace_in_server(server_path, fn_name, new_code)
            if not ok:
                print(f"  [WARN] could not locate {fn_name} in {server_path}", file=sys.stderr)
        print(f"[sync] Replaced {len(updated_tools)} tool(s) in {server_path}", file=sys.stderr)

    # 2. Append new tools
    if new_blocks:
        header = textwrap.dedent(f"""\
            # ============================================================
            # AUTO-GENERATED by nan_to_mcp.py
            # Source: {source}
            # Run `python nan_to_mcp.py --help` to regenerate.
            # Review all ⚠ sections before deploying.
            # ============================================================

        """)
        full_output = header + "\n\n".join(new_blocks)

        if server_path:  # --append-to or --sync
            server_text = server_path.read_text()
            marker = '\nif __name__ == "__main__":'
            if marker in server_text:
                pos = server_text.index(marker)
                new_text = server_text[:pos] + "\n\n" + full_output + server_text[pos:]
            else:
                new_text = server_text + "\n\n" + full_output
            server_path.write_text(new_text)
            print(f"[done] Appended {len(new_blocks)} new tool(s) to {server_path}", file=sys.stderr)

        elif args.output:
            out_path = Path(args.output).expanduser()
            out_path.write_text(full_output)
            print(f"[done] Wrote {len(new_blocks)} tool(s) to {args.output}", file=sys.stderr)

        else:
            print(full_output)

    elif not updated_tools:
        print("[info] Nothing to generate.", file=sys.stderr)

    # ── Summary ──────────────────────────────────────────────────────────────
    if skipped:
        print(f"\n[skipped {len(skipped)}]", file=sys.stderr)
        for s in skipped:
            print(s, file=sys.stderr)
    if failed:
        print(f"\n[failed  {len(failed)}]", file=sys.stderr)
        for f in failed:
            print(f"  {f}", file=sys.stderr)


if __name__ == "__main__":
    main()
