# ---------------------------------------------------------------------------
# Docstring extraction from Nan's module-level docstrings
# ---------------------------------------------------------------------------

def _parse_module_docstring(docstring: str) -> tuple[str, dict[str, str]]:
    """Parse Nan's module docstring into (description, param_hints).

    Input format:
        Tool: <Name> (<module>).

        <optional free-text description>

        Required: key1, key2, ...
        Optional: key1 (hint), key2 (hint), ...
        - special notes ...

    Returns:
        description : multi-sentence string to use as the function summary
        param_hints : {param_name: hint_string} extracted from Optional: lines
    """
    if not docstring:
        return "", {}

    lines = docstring.splitlines()
    desc_lines: list[str] = []
    optional_text_parts: list[str] = []
    in_optional = False

    for line in lines:
        stripped = line.strip()
        # Skip the "Tool: X (module)." header line
        if stripped.startswith("Tool:") or stripped.startswith("Tool "):
            continue
        # Start of Required: section — stop collecting description
        if re.match(r"Required\s*:", stripped, re.IGNORECASE):
            in_optional = False
            continue
        # Start of Optional: section
        if re.match(r"Optional\s*:", stripped, re.IGNORECASE):
            in_optional = True
            # Grab text after "Optional:"
            rest = re.sub(r"^Optional\s*:", "", stripped, flags=re.IGNORECASE).strip()
            if rest:
                optional_text_parts.append(rest)
            continue
        # Continuation of Optional: (indented or bullet)
        if in_optional:
            if stripped.startswith("-") or stripped.startswith("If ") or stripped.startswith("At least"):
                # End structured optional params, but keep as notes
                in_optional = False
            else:
                optional_text_parts.append(stripped)
            continue
        # Regular description lines
        if stripped:
            desc_lines.append(stripped)

    # Build description string — join non-empty lines
    description = " ".join(desc_lines).strip()

    # Parse optional param hints from collected optional text
    # e.g. "cc_method ('factors'|'intensity'), t_air_average_radius, ..."
    # e.g. "avg_grid_distance (km, default 4), max_depth (m, default 60),"
    param_hints: dict[str, str] = {}
    full_optional = " ".join(optional_text_parts)
    # Split on comma, but NOT commas inside parentheses
    # Simple approach: split on ", " followed by a word char
    tokens = re.split(r",\s*(?=[a-z_])", full_optional)
    for token in tokens:
        token = token.strip().rstrip(",. ")
        if not token:
            continue
        # Match: param_name (hint text) or param_name=value or just param_name
        m = re.match(r"^([a-z_][a-z0-9_]*)\s*(.*)$", token)
        if not m:
            continue
        pname = m.group(1)
        hint_raw = m.group(2).strip()
        # Clean up hint: strip outer parens if present
        if hint_raw.startswith("(") and hint_raw.endswith(")"):
            hint_raw = hint_raw[1:-1].strip()
        if hint_raw:
            param_hints[pname] = hint_raw
        else:
            param_hints[pname] = ""   # present but no hint

    return description, param_hints


