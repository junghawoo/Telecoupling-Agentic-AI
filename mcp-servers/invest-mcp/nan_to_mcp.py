def parse_nan_tool(filepath: Path) -> Optional[ToolSpec]:
    """Parse one of Nan's tool files and return a ToolSpec, or None on failure."""
    source = filepath.read_text()
    try:
        tree = ast.parse(source)
    except SyntaxError as e:
        print(f"  [SKIP] {filepath.name}: SyntaxError: {e}", file=sys.stderr)
        return None

    func_node = _find_async_run_function(tree)
    if not func_node:
        print(f"  [SKIP] {filepath.name}: no async run_* function found", file=sys.stderr)
        return None

    fn_name = func_node.name
    invest_module, invest_execute = _extract_invest_module(tree)
    required_keys: set[str] = set(_extract_required_keys(tree))

    is_custom = not bool(invest_module)

    if is_custom:
        # Non-InVEST tool: walk the whole function body for params["key"] /
        # params.get() patterns instead of an invest_args dict.
        raw_params = _extract_all_params_from_body(func_node.body, required_keys)
    else:
        local_vars = _collect_local_param_vars(func_node.body)
        raw_params = _extract_invest_args_dict(func_node.body, local_vars)

    # Build ordered param list: required first, optional second
    required_list: list[ToolParam] = []
    optional_list: list[ToolParam] = []
    seen: set[str] = set()

    for key, param in raw_params.items():
        if key == "results_suffix":
            continue
        seen.add(key)
        if param.required:
            required_list.append(param)
        else:
            optional_list.append(param)

    if is_custom:
        # Custom tools manage their own workspace via generate_output_dir();
        # no workspace_dir / results_suffix params needed in the MCP signature.
        pass
    else:
        # workspace_dir and results_suffix are always last for InVEST tools
        optional_list.append(ToolParam("workspace_dir", "str", "", required=False))
        optional_list.append(ToolParam("results_suffix", "str", "", required=False))

    # Detect complex logic that the converter couldn't fully capture
    has_complex = bool(
        required_keys and not any(p.required for p in required_list)
    ) or any(
        isinstance(stmt, ast.If) for stmt in func_node.body
        if not isinstance(stmt, (ast.Expr, ast.Assign, ast.Return, ast.Try))
    )

    raw_docstring = ast.get_docstring(tree) or ""
    doc_description, doc_param_hints = _parse_module_docstring(raw_docstring)

    # doc_summary = first free-text line that is NOT a Tool:/Required:/Optional: header
    _skip_prefixes = ("Tool:", "Tool ", "Required:", "Required ", "Optional:", "Optional ")
    doc_summary = ""
    for line in raw_docstring.splitlines():
        s = line.strip()
        if s and not any(s.startswith(p) for p in _skip_prefixes):
            doc_summary = s
            break

    return ToolSpec(
        function_name=fn_name,
        model_name=_model_name_from_function(fn_name),
        output_subdir=fn_name.removeprefix("run_"),
        invest_module=invest_module,
        invest_execute=invest_execute,
        params=required_list + optional_list,
        doc_summary=doc_summary,
        doc_description=doc_description,
        doc_param_hints=doc_param_hints,
        source_file=filepath.name,
        has_complex_logic=has_complex,
        is_custom_tool=is_custom,
    )


