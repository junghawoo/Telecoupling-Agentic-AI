def generate_mcp_tool(spec: ToolSpec) -> str:
    """Render a complete @mcp.tool() decorated function from a ToolSpec."""
    if spec.is_custom_tool:
        return generate_custom_mcp_tool(spec)
    lines: list[str] = []

    # Section header
    eq = "=" * 68
    lines.append(f"# {eq}")
    lines.append(f"# {spec.model_name}")
    lines.append(f"# (auto-generated from {spec.source_file} by nan_to_mcp.py)")
    if spec.has_complex_logic:
        lines.append(
            "# ⚠ REVIEW: source has conditional param logic — verify args dict below"
        )
    lines.append(f"# {eq}")

    # Decorator
    lines.append("@mcp.tool()")

    # Function signature
    required_params = [p for p in spec.params if p.required]
    optional_params = [p for p in spec.params if not p.required]

    sig_parts: list[str] = []
    for p in required_params:
        sig_parts.append(f"    {p.name}: {p.type_hint}")
    for p in optional_params:
        dv = _default_repr(p.type_hint, p.default)
        sig_parts.append(f"    {p.name}: {p.type_hint} = {dv}")

    if sig_parts:
        sig = "def {}(\n{},\n) -> str:".format(
            spec.function_name,
            ",\n".join(sig_parts)
        )
    else:
        sig = f"def {spec.function_name}() -> str:"
    lines.append(sig)

    # ── Docstring ─────────────────────────────────────────────────────────────
    # Priority for the opening summary:
    #   1. Official readthedocs description  (from invest_docs_cache.json)
    #   2. Curated _MODEL_DESCRIPTIONS entry
    #   3. Free-text extracted from Nan's module docstring
    #   4. First non-header line of Nan's module docstring
    #   5. Generic fallback
    official = INVEST_DOCS.get(spec.function_name, {})
    official_desc = official.get("description", "").strip()
    official_params: dict[str, str] = official.get("params", {})

    if official_desc:
        opening = official_desc
    elif spec.function_name in _MODEL_DESCRIPTIONS:
        opening = _MODEL_DESCRIPTIONS[spec.function_name]
    elif spec.doc_description:
        opening = spec.doc_description
    elif spec.doc_summary:
        opening = spec.doc_summary
    else:
        opening = f"Run the InVEST {spec.model_name} model."

    # Wrap to 79 chars for the summary paragraph
    wrapped = textwrap.fill(opening, width=75, subsequent_indent="    ")
    doc: list[str] = [f'    """{wrapped}', ""]

    # Per-param description helper — four-layer priority:
    #   1. Official readthedocs docs  (official_params from cache)
    #   2. Curated _PARAM_DESCRIPTIONS lookup
    #   3. Nan's Optional: hint from module docstring
    #   4. Name-pattern fallback
    def _param_desc(p: ToolParam) -> str:
        # 1. Official readthedocs description (highest authority)
        if p.name in official_params:
            return official_params[p.name]

        # 2. Curated hand-written lookup
        if p.name in _PARAM_DESCRIPTIONS:
            base = _PARAM_DESCRIPTIONS[p.name]
            hint = spec.doc_param_hints.get(p.name, "")
            if hint and hint.lower() not in base.lower():
                if "|" in hint or hint.startswith("'") or re.match(r"^[a-z]", hint):
                    base = f"{base} ({hint})"
            return base

        # 3. Nan's Optional: hint
        hint = spec.doc_param_hints.get(p.name, "")
        if hint:
            return hint[0].upper() + hint[1:]

        # 4. Name-pattern fallback
        name = p.name
        if name.endswith("_raster_path"):
            return f"Path to {name[:-12].replace('_', ' ')} raster"
        if name.endswith("_vector_path"):
            return f"Path to {name[:-12].replace('_', ' ')} vector"
        if name.endswith("_table_path"):
            return f"Path to {name[:-11].replace('_', ' ')} CSV table"
        if name.endswith("_path"):
            return f"Path to {name[:-5].replace('_', ' ')} file"
        if name.endswith("_csv"):
            return f"Path to {name[:-4].replace('_', ' ')} CSV"
        if name.endswith("_dir"):
            return f"Path to {name[:-4].replace('_', ' ')} directory"
        if name.startswith("do_") or name.startswith("use_") or name.startswith("calc_"):
            return f"If True, {name.split('_', 1)[1].replace('_', ' ')}"
        if name.startswith("calculate_"):
            return f"If True, compute {name[10:].replace('_', ' ')} raster"
        dv = _default_repr(p.type_hint, p.default)
        return f"Required {p.type_hint}" if p.required else f"Optional {p.type_hint}, default {dv}"

    doc.append("    Args:")
    for p in spec.params:
        desc = _param_desc(p)
        # Wrap long descriptions at 79 chars
        line = f"        {p.name}: {desc}"
        if len(line) > 88:
            wrapped_desc = textwrap.fill(
                desc, width=75,
                initial_indent=f"        {p.name}: ",
                subsequent_indent="            ",
            )
            doc.append(wrapped_desc)
        else:
            doc.append(line)

    doc.append('    """')
    lines.append("\n".join(doc))

    # Import
    if spec.invest_module:
        lines.append(f"    import {spec.invest_module}")

    lines.append("")

    # Workspace
    lines.append(
        f'    ws = ensure_workspace(workspace_dir, os.path.join(OUTPUT_DIR, "{spec.output_subdir}"))'
    )

    # Build args dict
    args_lines = ["    args = {"]
    for p in spec.params:
        if p.name in ("workspace_dir", "results_suffix"):
            continue
        args_lines.append(f'        "{p.name}": {p.name},')
    args_lines.append('        "workspace_dir": ws,')
    args_lines.append('        "results_suffix": results_suffix,')
    args_lines.append("    }")
    lines.append("\n".join(args_lines))

    # Execute via run_invest_model helper
    lines.append("")
    if spec.invest_module:
        lines.append(
            f'    return run_invest_model("{spec.model_name}", {spec.invest_module}, args, ws)'
        )
    else:
        # Non-InVEST tool: emit a stub body that raises so Nan can fill it in
        lines.append(f'    # ⚠ TODO: {spec.source_file} has no natcap.invest import.')
        lines.append(f'    # Replace this stub with the real implementation.')
        lines.append(f'    raise NotImplementedError("{spec.function_name} requires a custom implementation — see {spec.source_file}")')

    return "\n".join(lines) + "\n"


def generate_stub(filepath: Path) -> str:
    """For non-InVEST utilities, emit a commented stub."""
    fn_name = filepath.stem
    if not fn_name.startswith("run_"):
        fn_name = f"run_{fn_name}"
    return textwrap.dedent(f"""\
        # {'='*68}
        # {fn_name}  ← NON-InVEST utility
        # Source: {filepath.name}
        # This module does not use natcap.invest — implement manually.
        # {'='*68}
        # @mcp.tool()
        # def {fn_name}(...) -> str:
        #     \"\"\"TODO: implement from {filepath.name}\"\"\"
        #     ...

    """)


