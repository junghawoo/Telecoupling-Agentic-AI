#!/usr/bin/env python3
"""
nan_to_mcp.py
~~~~~~~~~~~~~
Converts Nan's async tool modules (PythonScripts_TCToolbox style) into
FastMCP @mcp.tool() functions compatible with invest-mcp/server.py.

Handles every pattern found across Nan's 29 tool files:

  Pattern 1 – Direct required param
      invest_args["key"] = params["key"]
      → def run_xxx(key: str, ...)

  Pattern 2 – Optional with default (.get)
      invest_args["key"] = params.get("key", default)
      → def run_xxx(key: str = "default", ...)

  Pattern 3 – Cast wrapper on required param
      invest_args["key"] = int(params["key"])
      → def run_xxx(key: int, ...)

  Pattern 4 – Local variable (traces back to .get)
      cc_method = params.get("cc_method", "factors")
      invest_args["cc_method"] = cc_method
      → def run_xxx(cc_method: str = "factors", ...)

  Pattern 5 – Conditional block addition
      if snap_points:
          invest_args["flow_threshold"] = params.get("flow_threshold", 1000)
      → def run_xxx(flow_threshold: int = 1000, ...)   (always included, InVEST ignores extra keys)

  Key renames (scenic_quality: "aoi_path" ← params["aoi_vector_path"])
      → uses INVEST key as function param name so no remapping needed

Usage
-----
    # Preview generated code for one module (stdout)
    python nan_to_mcp.py path/to/coastal_vulnerability.py

    # Convert all new tools and write to a file
    python nan_to_mcp.py path/to/tools/ --output new_tools.py

    # Append only tools NOT already in server.py
    python nan_to_mcp.py path/to/tools/ --append-to server.py

    # Dry-run: show which files would be converted
    python nan_to_mcp.py path/to/tools/ --dry-run

    # Explicitly skip certain modules
    python nan_to_mcp.py path/to/tools/ --skip cbc_main cbc_preprocessor habitat_quality
"""

