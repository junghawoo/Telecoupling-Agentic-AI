def _infer_type(key: str, default: Any) -> str:
    """Infer Python type annotation from default value first, then key name."""
    if isinstance(default, bool):
        return "bool"
    if isinstance(default, int):
        return "int"
    if isinstance(default, float):
        return "float"
    if isinstance(default, str):
        return "str"
    k = key.lower()
    for p in _BOOL_PREFIXES:
        if k.startswith(p):
            return "bool"
    for s in _STR_SUFFIXES:
        if k.endswith(s):
            return "str"
    for kw in _INT_KEYWORDS:
        if kw in k:
            return "int"
    for kw in _FLOAT_KEYWORDS:
        if kw in k:
            return "float"
    return "str"


def _default_repr(t: str, original: Any) -> str:
    """Render a default value as Python source code."""
    if original is not None:
        if isinstance(original, bool):
            return str(original)
        if isinstance(original, str):
            if original == "":
                return '""'
            return f'"{original}"'
        if isinstance(original, (int, float)):
            return str(original)
    # Fallback per type
    return {"bool": "False", "int": "0", "float": "0.0", "str": '""'}[t]


