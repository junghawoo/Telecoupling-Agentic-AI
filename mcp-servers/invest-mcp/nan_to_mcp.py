def _fn_name_for_stem(stem: str) -> str:
    """Map a tool file stem to its expected MCP function name."""
    return {"cbc_main": "run_coastal_blue_carbon",
            "cbc_preprocessor": "run_coastal_blue_carbon_preprocessor"}.get(stem, f"run_{stem}")


def _param_signature(node: ast.FunctionDef) -> tuple:
    """Return a comparable representation of a function's parameter list.

    Captures (name, annotation_str, default_str) for every argument so that
    any change Nan makes — adding/removing a param, changing a type hint or
    default value — is detected as a signature difference.
    """
    # Pad defaults to align with args (defaults cover the *last* N args)
    n_args = len(node.args.args)
    n_defs = len(node.args.defaults)
    pad = n_args - n_defs
    defaults = [None] * pad + node.args.defaults

    rows = []
    for arg, default in zip(node.args.args, defaults):
        ann = ast.unparse(arg.annotation) if arg.annotation else ""
        dfl = ast.unparse(default)        if default    else ""
        rows.append((arg.arg, ann, dfl))
    return tuple(rows)


def _server_signatures(path: Path) -> dict[str, tuple]:
    """Return {fn_name: _param_signature()} for every run_* function in path."""
    sigs: dict[str, tuple] = {}
    try:
        tree = ast.parse(path.read_text())
        for node in ast.walk(tree):
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)) and node.name.startswith("run_"):
                sigs[node.name] = _param_signature(node)
    except Exception:
        pass
    return sigs


def _existing_tools_in_file(path: Path) -> set[str]:
    """Return the set of run_* function names defined in path."""
    return set(_server_signatures(path).keys())


def _replace_in_server(server_path: Path, fn_name: str, new_code: str) -> bool:
    """Replace the existing @mcp.tool() function *fn_name* in server_path
    with *new_code*, including its preceding section-header comment block.

    Uses Python 3.8+ ``end_lineno`` AST attribute for exact line boundaries.
    Returns True if the replacement was made, False if the function was not found.
    """
    source = server_path.read_text()
    lines  = source.splitlines(keepends=True)

    try:
        tree = ast.parse(source)
    except SyntaxError:
        return False

    for node in ast.walk(tree):
        if not (isinstance(node, ast.FunctionDef) and node.name == fn_name):
            continue

        # --- locate decorator / function boundaries (1-indexed AST → 0-indexed slice) ---
        dec_line0 = (node.decorator_list[0].lineno - 1
                     if node.decorator_list else node.lineno - 1)
        end_line0 = node.end_lineno   # 1-indexed inclusive → 0-indexed exclusive

        # --- walk backward to absorb the section-header comment block ---
        # We accept blank lines and comment lines; stop on any other content.
        block_start = dec_line0
        for i in range(dec_line0 - 1, max(-1, dec_line0 - 20), -1):
            stripped = lines[i].strip()
            if stripped == "" or stripped.startswith("#"):
                block_start = i
            else:
                break

        # --- replace the block with the new code ---
        replacement = new_code.rstrip("\n") + "\n\n"
        lines[block_start:end_line0] = [replacement]
        server_path.write_text("".join(lines))
        return True

    return False


def _signature_diff(old_sig: tuple, new_sig: tuple) -> str:
    """Return a human-readable one-line description of what changed."""
    old_names = {r[0] for r in old_sig}
    new_names = {r[0] for r in new_sig}
    added   = new_names - old_names
    removed = old_names - new_names
    changed = {r[0] for r in new_sig if r[0] in old_names
               and r != next((o for o in old_sig if o[0] == r[0]), None)}
    parts = []
    if added:   parts.append(f"+{{{', '.join(sorted(added))}}}")
    if removed: parts.append(f"-{{{', '.join(sorted(removed))}}}")
    if changed: parts.append(f"~{{{', '.join(sorted(changed))}}}")
    return "  ".join(parts) if parts else "default or type changed"


