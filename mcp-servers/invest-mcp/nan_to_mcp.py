def _const(node) -> Optional[Any]:
    """Extract scalar constant from AST node, handling negation."""
    if isinstance(node, ast.Constant):
        return node.value
    if isinstance(node, ast.UnaryOp) and isinstance(node.op, ast.USub):
        if isinstance(node.operand, ast.Constant):
            return -node.operand.value
    return None


def _cast_name(node) -> Optional[str]:
    """Return 'int', 'float', or 'bool' if node is a cast call; else None."""
    if isinstance(node, ast.Call) and isinstance(node.func, ast.Name):
        if node.func.id in ("int", "float", "bool"):
            return node.func.id
    return None


def _is_params_subscript(node) -> bool:
    """True if node is params["key"] or params[key]."""
    return (
        isinstance(node, ast.Subscript)
        and isinstance(node.value, ast.Name)
        and node.value.id == "params"
    )


def _is_params_get(node) -> bool:
    """True if node is params.get(...)."""
    return (
        isinstance(node, ast.Call)
        and isinstance(node.func, ast.Attribute)
        and node.func.attr == "get"
        and isinstance(node.func.value, ast.Name)
        and node.func.value.id == "params"
    )


def _extract_params_get(node) -> tuple[str, Any]:
    """From params.get("key", default) return (key, default). default=None if absent."""
    key = _const(node.args[0]) if node.args else None
    default = _const(node.args[1]) if len(node.args) >= 2 else None
    return key, default


